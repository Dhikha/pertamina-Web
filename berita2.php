                <p>Jakarta, 1 Januari 2022 – PT Pertamina Hulu Indonesia (PHI) sebagai Regional Kalimantan Subholding Upstream berhasil melaksanakan pemberdayaan masyarakat secara berkelanjutan melalui program-program tanggung jawab sosial dan lingkungan yang dijalankan oleh lapangan-lapangan.
                <br><br>
                Masing-masing lapangan mempunyai keunikan programnya masing-masing dengan mengacu kepada tujuan program berkelanjutan/Sustainable Development Goals (SDGs). Seperti lapangan PT Pertamina Hulu Mahakam (PHM)-BSP dengan program Petani Maju 4.0, yaitu program peningkatan kapasitas petani muda lokal melalui inovasi pertakultur; dan lapangan PHM-SPU dengan program Nelayanku Hebat, yang mengembangkan inovasi apartemen ikan dan fishfinder sebagai bagian dari sistem perikanan ramah lingkungan pesisir Delta Mahakam.
                <br><br>
                Sementara lapangan PT Pertamina Hulu Kalimatan Timur (PHKT)-DOBS memiliki program Kampung Kopi Luwak Desa Prangat Baru (Kapak Prabu), melalui kegiatan pemberdayaan kelompok tani kampung kopi luwak yang terorganisir, agar dapat menjadi ikon eduwisata kopi luwak liberika. 
                <br><br>
                Di lapangan lainnya, yaitu PT Pertamina EP (PEP) Sangasanga Field terdapat program Tante Siska, yaitu Program Tani Terpadu Sistem Inovasi Sosial Kelompok Setaria; serta PEP Tarakan Field yang mengusung program Kubedistik, yaitu program Rumah Batik Disabilitas Tarakan. 
                <br><br>
                Seluruh program unggulan tersebut menerapkan inovasi sosial dan berkelanjutan sehingga pada tahun 2021, program-program tersebut berhasil mendukung pencapaian 5 penghargaan PROPER Emas dari Kementerian Lingkungan Hidup dan Kehutanan (KLHK). PROPER merupakan Program Penilaian Peringkat Kinerja Perusahaan Dalam Pengelolaan Lingkungan yang dikembangkan oleh Kementerian Lingkungan Hidup (KLH) dimana PROPER emas merupakan predikat tertinggi. 
                <br><br>
                Nomor SK.69/PPKL/SET/WAS.8/11/2021 Tentang Penetapan Kandidat Emas Program Penilaian Peringkat Kinerja Perusahaan dalam Pengelolaan Lingkungan Hidup Periode Tahun 2020-2021, terdapat entitas di bawah pengelolaan PHI, yang mendapatkan predikat PROPER emas dan PROPER hijau tahun 2021 yaitu: 5 Lapangan PROPER Emas dan 11 Lapangan PROPER Hijau.
                <br><br>
                Direktur Utama PHI-Regional Kalimantan, Chaid Said Salim, menyampaikan bahwa pencapaian PROPER pada tahun 2021 ini membuktikan komitmen PHI dalam ESG (environmental, social, and corporate governance). “Keberhasilan ini tentunya diraih dengan perencanaan, pelaksanaan, pengawasan, evaluasi, dan publikasi positif terhadap program-program unggulan tanggung jawab sosial dan lingkungan yang sangat baik dengan sinergi PHI dan seluruh pemangku kepentingan,” ungkap Chalid.
                <br><br>
                “Ajang PROPER Tahun 2021 memberikan dorongan bagi kalangan usaha migas, termasuk PHI, agar terus berupaya melakukan pengembangan dan pelibatan masyarakat beserta pemangku kepentingan eksternal terkait dalam menjalankan program tanggung jawab sosial dan lingkungan. Salah satu kriteria penilaian PROPER tahun ini yang menjadi perhatian khusus dari Dewan PROPER adalah inovasi sosial. Melalui inovasi sosial, diharapkan perusahaan mampu memahami secara penuh permasalahan sosial di sekitar wilayah kerja dan menyelesaikan masalah tersebut dengan tepat,” imbuh Chalid.
