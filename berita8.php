                <p>BOJONEGORO - PT Pertamina EP Cepu (PEPC) Zona 12 (JTB) Regional Indonesia Timur Subholding Upstream Pertamina terus mendukung pertanian yang berkelanjutan agar kesejahteraan masyarakat sekitar wilayah operasi dapat terjaga. Melalui Program Pengembangan Masyarakat (PPM), PEPC JTB sejak pertengahan tahun ini melaksanakan Program Peningkatan Kualitas Pertanian (kualitas cekdam, dinding embung, sumur bor, pompanisasi dan pipanisasi pertanian) yang tersebar di lima desa di Bojonegoro. 
