                Sri menyampaikan harapannya agar bantuan yang diberikan dapat disalurkan kepada warga masyarakat yang memerlukannya. "Semoga amanah yang kami terima dari para pekerja untuk menyalurkannya kepada yang berhak, juga dapat diteruskan di posko ini sehingga benar-benar dapat diterima oleh masyarakat yang benar-benar paling memerlukannya," harap Sri.
