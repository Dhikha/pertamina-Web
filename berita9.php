                <p>BALIKPAPAN - Kepedulian untuk masyarakat terdampak korban kebakaran di Kelurahan Baru Ilir yang terjadi beberapa waktu yang lalu terus ditunjukkan Pertamina. Kali ini, Baituzzakah (Bazma) Pertamina RU V Balikpapan menyalurkan bantuan berupa  80 kompor, 80 selang & regulator kompor gas, 50 tas sekolah serta bantuan dana masing-masing Rp100 ribu untuk 50 anak, Selasa, 21 Desember 2021.
