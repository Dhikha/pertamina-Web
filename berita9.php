                Sementara itu, Area Manager Communication, Relations & CSR PT Kilang Pertamina Internasional (KPI) Unit Balikpapan Ely Chandra Peranginangin menambahkan, memang ada beberapa bagian dari KPI Unit Balikpapan yang juga ingin berpartisipasi dalam membantu masyarakat terdampak.
                <br><br>
                "Kemarin, Persatuan Wanita Patra Tingkat RU Balikpapan juga turut memberikan bantuan kepada masyarakat yang terdampak. Begitu juga dengan para pekerja muda Pertamina yang tergabung dalam Agent of Change (AOC) yang saat ini sedang melakukan penggalangan donasi," kata Chandra.
                <br><br>
                Bentuk-bentuk kepedulian ini menurut Chandra merupakan salah satu wujud kebersamaan. "Banyaknya pihak yang terlibat ingin membantu menunjukkan kepedulian kepada sesama itu memang nyata ada, dan hal itu perlu terus kita dukung dan kembangkan," tutupnya.</p>
